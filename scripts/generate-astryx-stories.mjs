#!/usr/bin/env node
/**
 * Generates Storybook stories for Astryx components and page patterns
 * from @astryxdesign/cli block/page templates.
 *
 * Run: vp run generate:astryx-stories
 */

import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";

import { component, template } from "@astryxdesign/cli/api";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUT = path.join(ROOT, "src/storybook/astryx/generated");
const COMPONENTS_DIR = path.join(OUT, "components");
const PATTERNS_DIR = path.join(OUT, "patterns");
const PAGES_ROOT = path.join(ROOT, "node_modules/@astryxdesign/cli/templates/pages");

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function cleanDir(dir) {
  if (fs.existsSync(dir)) {
    for (const file of fs.readdirSync(dir)) {
      fs.unlinkSync(path.join(dir, file));
    }
  } else {
    fs.mkdirSync(dir, { recursive: true });
  }
}

function escapeString(value) {
  return value.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");
}

function toValidExportName(raw) {
  let name = raw
    .replace(/[^a-zA-Z0-9]+/g, " ")
    .trim()
    .split(/\s+/)
    .filter(Boolean)
    .map((part) => (/^\d/.test(part) ? `N${part}` : part))
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");

  if (!name || !/^[A-Za-z_]/.test(name)) {
    name = `Story${name || "Example"}`;
  }

  return name;
}

function toStoryExportName(blockName, isShowcase) {
  if (isShowcase) return "Showcase";
  const rest = blockName.replace(/^[A-Z][a-z]+/, "");
  if (rest && rest !== blockName) {
    return toValidExportName(rest.replace(/Showcase$/, "") || blockName);
  }
  return toValidExportName(blockName);
}

function blockImportPath(category, blockName) {
  return `@astryx/blocks/${category}/${blockName}`;
}

function blockImportAlias(blockName) {
  const base = `${blockName}Block`;
  return /^[a-z]/.test(base) ? base.charAt(0).toUpperCase() + base.slice(1) : base;
}

function blockToExport(block, usedNames) {
  let name = toStoryExportName(block.name, block.isShowcase);
  if (block.displayName && !block.isShowcase) {
    const slug = block.displayName.replace(/^[^—]+—\s*/, "");
    name = toValidExportName(slug || block.name);
  }
  let candidate = name;
  let i = 2;
  while (usedNames.has(candidate)) {
    candidate = `${name}${i}`;
    i += 1;
  }
  usedNames.add(candidate);
  return candidate;
}

async function generateComponentStories() {
  const list = await component(undefined, { list: true, detail: "compact" });
  const entries = Object.values(list.data).flat();

  let count = 0;
  for (const entry of entries) {
    const name = entry.name;
    const [blocksResult, detailResult] = await Promise.all([
      component(name, { blocks: true }),
      component(name),
    ]);

    const { showcase, examples } = blocksResult.data;
    const blockList = [
      ...(showcase ? [{ ...showcase, isShowcase: true }] : []),
      ...(examples ?? []),
    ];

    const description = detailResult.data?.description ?? entry.description ?? "";
    const usedExports = new Set(["Default"]);
    const imports = [];
    const stories = [];

    for (const block of blockList) {
      const exportName = blockToExport(block, usedExports);
      const importAlias = blockImportAlias(block.name);
      const importPath = blockImportPath(block.category, block.name);
      imports.push(`import ${importAlias} from "${importPath}";`);
      const padded = block.category.includes("AppShell") ? "false" : "true";
      stories.push(`export const ${exportName}: Story = {
  name: ${JSON.stringify(block.displayName || block.name)},
  render: () => (
    <BlockStory padded={${padded}}>
      <${importAlias} />
    </BlockStory>
  ),
};`);
    }

    if (stories.length === 0) {
      imports.push(`import { Text } from "@astryxdesign/core/Text";`);
      stories.push(`export const Overview: Story = {
  render: () => (
    <BlockStory>
      <Text color="secondary">No block template available for ${name}. See docs for props and usage.</Text>
    </BlockStory>
  ),
};`);
    }

    const fileName = `${name}.stories.tsx`;
    const content = `/* eslint-disable */
// Generated by scripts/generate-astryx-stories.mjs — do not edit manually.

import type { Meta, StoryObj } from "@storybook/react-vite";

import { BlockStory } from "../../shared/block-story";
${imports.join("\n")}

const meta = {
  title: "Astryx/Components/${name}",
  tags: ["autodocs"],
  parameters: {
    docs: {
      description: {
        component: \`${escapeString(description)}\`,
      },
    },
  },
} satisfies Meta;

export default meta;
type Story = StoryObj<typeof meta>;

${stories.join("\n\n")}
`;

    fs.writeFileSync(path.join(COMPONENTS_DIR, fileName), content);
    count += 1;
  }

  return count;
}

function patternGroup(name) {
  if (/^login|^login-/.test(name)) return "Auth";
  if (/^table|^table-/.test(name)) return "Tables";
  if (/^dashboard/.test(name)) return "Dashboard";
  if (/^form|^contact|^payment/.test(name)) return "Forms";
  if (/^doc|^documentation/.test(name)) return "Documentation";
  if (/^ai-/.test(name)) return "AI";
  if (/^product|^gallery|^mixed|^classic|^centered|^side-/.test(name)) return "Gallery";
  if (/^settings/.test(name)) return "Settings";
  if (/^detail|^editor|^ide|^file-|^blank|^theme-/.test(name)) return "Layout";
  return "General";
}

function pageRequiresLab(pageName) {
  const pagePath = path.join(PAGES_ROOT, pageName, "page.tsx");
  if (!fs.existsSync(pagePath)) return false;
  return fs.readFileSync(pagePath, "utf8").includes("@astryxdesign/lab");
}

async function generatePatternStories() {
  const list = await template(undefined, { list: true });
  const pages = list.data.filter((t) => t.type === "page");

  let count = 0;
  for (const page of pages) {
    const group = patternGroup(page.name);
    const title = `Astryx/Patterns/${group}/${page.displayName || page.name}`;
    const description = page.description ?? "";
    const requiresLab = pageRequiresLab(page.name);

    const content = requiresLab
      ? `/* eslint-disable */
// Generated by scripts/generate-astryx-stories.mjs — do not edit manually.

import type { Meta, StoryObj } from "@storybook/react-vite";

import { Text } from "@astryxdesign/core/Text";

import { DocsPage } from "../../shared/docs-page";

const meta = {
  title: ${JSON.stringify(title)},
  tags: ["autodocs"],
  parameters: {
    layout: "fullscreen",
    docs: {
      description: {
        component: ${JSON.stringify(description)} + " Requires @astryxdesign/lab (optional peer) — not installed in this project yet.",
      },
    },
  },
} satisfies Meta;

export default meta;
type Story = StoryObj<typeof meta>;

export const Unavailable: Story = {
  name: "Requires @astryxdesign/lab",
  render: () => (
    <DocsPage
      title=${JSON.stringify(page.displayName || page.name)}
      description="This page template uses chart components from @astryxdesign/lab. Install a lab version compatible with your @astryxdesign/core release to preview it here."
    >
      <Text color="secondary">
        Template id: <code>${page.name}</code>
      </Text>
    </DocsPage>
  ),
};
`
      : `/* eslint-disable */
// Generated by scripts/generate-astryx-stories.mjs — do not edit manually.

import type { Meta, StoryObj } from "@storybook/react-vite";

import PageTemplate from "@astryx/pages/${page.name}/page";

const meta = {
  title: ${JSON.stringify(title)},
  tags: ["autodocs"],
  parameters: {
    layout: "fullscreen",
    docs: {
      description: {
        component: ${JSON.stringify(description)},
      },
    },
  },
} satisfies Meta<typeof PageTemplate>;

export default meta;
type Story = StoryObj<typeof meta>;

export const Page: Story = {
  render: () => <PageTemplate />,
};
`;

    fs.writeFileSync(path.join(PATTERNS_DIR, `${page.name}.stories.tsx`), content);
    count += 1;
  }

  return count;
}

async function main() {
  ensureDir(OUT);
  cleanDir(COMPONENTS_DIR);
  cleanDir(PATTERNS_DIR);

  const componentCount = await generateComponentStories();
  const patternCount = await generatePatternStories();

  console.log(`Generated ${componentCount} component story files`);
  console.log(`Generated ${patternCount} pattern story files`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
